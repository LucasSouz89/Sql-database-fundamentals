Processam cada registro individualmente e retornam um valor para cada linha.

UCASE(): Transforma o texto de um campo para letras maiusculas.
LCASE(): Transforma o texto de um campo para letras minusculas.
MID(): Extrai um trecho (subtexto) de um campo de texto.
LEN(): Retorna a quantidade de caracteres (tamanho) de um texto.
ROUND(): Arredonda um numero decimal para um numero especifico de casas.
NOW(): Retorna a data e a hora exatas do sistema no momento da execucao.
FORMAT(): Define como um campo deve ser exibido, aplicando mascaras de formato.
